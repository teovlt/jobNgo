/**
 * Enum for log levels.
 *
 * @typedef {Object} logLevels
 * @property {string} INFO - Log level for informational messages.
 * @property {string} WARN - Log level for warning messages.
 * @property {string} ERROR - Log level for error messages.
 * @property {string} DEBUG - Log level for debug messages.
 */
export enum logLevels {
  INFO = "info",
  WARN = "warn",
  ERROR = "error",
  DEBUG = "debug",
}
